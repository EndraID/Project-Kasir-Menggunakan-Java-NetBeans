    private void TambahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahBtnActionPerformed
       String namaBarang = TxtNamaBarang.getText();
        int hargaBarang = Integer.parseInt(TxtHarga.getText());
        int stockBarang = Integer.parseInt(TxtStock.getText());


        String query = "INSERT INTO barang (nama_barang, harga_barang, stock_barang) VALUES (?, ?, ?)";

        
            try {
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, namaBarang);
            pst.setInt(2, hargaBarang);
            pst.setInt(3, stockBarang);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
            loadTable();
            TxtNamaBarang.setText("");
            txtIdBarang.setText("");
            TxtHarga.setText("");
            TxtStock.setText("");
            
        } catch (SQLException e) {
            e.printStackTrace();
        }  
        
        
        
    }//GEN-LAST:event_TambahBtnActionPerformed

    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        
         int selectedRow = TableBarang.getSelectedRow();
          if (selectedRow != -1) {
            int idBarang = (int) tableModel.getValueAt(selectedRow, 0);
            String namaBarang = TxtNamaBarang.getText();
            int hargaBarang = Integer.parseInt(TxtHarga.getText());
            int stockBarang = Integer.parseInt(TxtStock.getText());
            
            String query = "UPDATE barang SET nama_barang = ?, harga_barang = ?, stock_barang = ? WHERE id_barang = ?";
            
            
             try {
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setString(1, namaBarang);
                pst.setInt(2, hargaBarang);
                pst.setInt(3, stockBarang);
                pst.setInt(4, idBarang);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
                loadTable();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diupdate");
            
            
          }
         
       
         
        
    }//GEN-LAST:event_BtnUpdateActionPerformed

