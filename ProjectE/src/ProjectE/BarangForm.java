    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = TableBarang.getSelectedRow();
            if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus!");
            return;
        }
            int idBarang = (int) tableModel.getValueAt(selectedRowIndex, 0);
            String query = "DELETE FROM barang WHERE id_barang = ?";
            
        try {
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setInt(1, idBarang);
        
        int deletedRows = pst.executeUpdate();
        
        if (deletedRows > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            loadTable(); // Reload table setelah hapus
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data!");
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_BtnHapusActionPerformed

